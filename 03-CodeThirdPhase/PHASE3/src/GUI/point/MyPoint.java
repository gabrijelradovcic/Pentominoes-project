package GUI.point;

public class MyPoint {

    public double x,y,z;
    
    /**
     * Creates a poin in a 3D space
     * @param x x axis
     * @param y y axis
     * @param z z axis
     */
    public MyPoint(double x, double y, double z){

        this.x = x;
        this.y = y;
        this.z = z;
    }
}